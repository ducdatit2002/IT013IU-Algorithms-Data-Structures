class Arithmetic 
{

   public static void main (String[] args) {

     System.out.println ("17 / 5 = " + (17 / 5));
     System.out.println ("17.0 / 5 = " + (17.0 / 5));
     System.out.println ("17 / 5.0 = " + (17 / 5.0));
     System.out.println ("9 / 12 = " + (9 / 12));
     System.out.println ("9.0 / 12.0 = " + (9.0 / 12));
     System.out.println ("1 / 2 = " + (1 / 2));
     System.out.println ("1 % 2 = " + (1 % 2));
     System.out.println ("14 % 5 = " + (14 % 5));
     System.out.println ("-14 % 5 = " + (-14 % 5));
     System.out.println ("14 % -5 = " + (14 % -5));
     System.out.println ("5 + 12 / 5 - 10 % 3 = " + (5 + 12 / 5 - 10 % 3));
   }

}

